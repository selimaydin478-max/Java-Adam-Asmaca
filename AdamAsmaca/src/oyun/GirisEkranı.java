    private void btnGirisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGirisActionPerformed
    String girilenSifre = new String(txtSifre.getPassword());
        String zaman = LocalDateTime.now().format(DateTimeFormatter.ofPattern("dd-MM-yyyy HH:mm:ss"));
        
        try {
            // Log dosyasına yazma işlemi (true parametresi ile üzerine ekleme yapar)
            FileWriter logYazici = new FileWriter(TXT_YOLU + "log.txt", true);
            
            if (girilenSifre.equals(kayitliSifre)) {
                // Şifre doğruysa
                logYazici.write("Başarılı giriş yapıldı - Tarih: " + zaman + "\n");
                logYazici.close();
                
                JOptionPane.showMessageDialog(this, "Giriş Başarılı!");
                // Adım 4'te Ana Oyun Ekranını buraya bağlayacağız.
                 this.dispose();
                 new OyunEkranı().setVisible(true);
                
            } else {
                // Şifre yanlışsa
                hataSayisi++;
                logYazici.write("Hatalı şifre denemesi - Tarih: " + zaman + "\n");
                logYazici.close();
                
                if (hataSayisi >= 3) {
                    JOptionPane.showMessageDialog(this, "3 kez hatalı giriş yaptınız. Program kapanıyor.");
                    System.exit(0);
                } else {
                    JOptionPane.showMessageDialog(this, "Hatalı şifre! Kalan hakkınız: " + (3 - hataSayisi));
                    txtSifre.setText(""); // Kutuyu temizle
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Log dosyasına yazılamadı: " + e.getMessage());
        }    // TODO add your handling code here:
    }//GEN-LAST:event_btnGirisActionPerformed

