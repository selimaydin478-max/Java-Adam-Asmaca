    public GirisEkranı() {
        initComponents();
        // Ekran açılır açılmaz şifre dosyasını kontrol et
        try {
            File sifreDosyasi = new File(TXT_YOLU + "sifre.txt");
            if (!sifreDosyasi.exists()) {
                sifreDosyasi.createNewFile();
            }
            
            Scanner okuyucu = new Scanner(sifreDosyasi);
            if (okuyucu.hasNextLine()) {
                // Şifre önceden belirlenmişse hafızaya al
                kayitliSifre = okuyucu.nextLine();
            } else {
                // Şifre dosyası boşsa, ilk şifreyi kullanıcıdan iste ve kaydet
                String yeniSifre = JOptionPane.showInputDialog(this, "İlk girişiniz. Lütfen bir şifre belirleyin:");
                if (yeniSifre != null && !yeniSifre.trim().isEmpty()) {
                    FileWriter yazici = new FileWriter(sifreDosyasi);
                    yazici.write(yeniSifre);
                    yazici.close();
                    kayitliSifre = yeniSifre;
                    JOptionPane.showMessageDialog(this, "Şifreniz başarıyla kaydedildi!");
                } else {
                    System.exit(0); // Şifre belirlemeden iptal ederse çık
                }
            }
            okuyucu.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Şifre dosyası okunurken hata oluştu: " + e.getMessage());
        }
    }

